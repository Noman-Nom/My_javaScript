
// switch cases in js 
// it is more effiecient then if else statement 

const month = 4;

switch (month) {
    case 1:
        console.log("jan")
        break;
    case 2:
        console.log("feb")
        break;
    case 3:
        console.log("march")
        break;
    case 4:
        console.log("April")
        break;

    default:
        break;
}